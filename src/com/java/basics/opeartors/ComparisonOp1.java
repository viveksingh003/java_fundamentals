package com.java.basics.opeartors;

public class ComparisonOp1 {
    public static void main(String[] args) {

        int a = 10;
        int b = 1;
        System.out.println(a > b);//t
        System.out.println(10 > 10);//f
        System.out.println(10 >= 10);//t
        System.out.println(10 != 10);//f
        System.out.println(10 == 10);//t
    }
}
