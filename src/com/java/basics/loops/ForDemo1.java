package com.java.basics.loops;

public class ForDemo1 {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        int j = 0;
        for (System.out.println("hello"); j < 10; j++) {
            System.out.println(j);
        }
    }
}
